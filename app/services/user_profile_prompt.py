from app.models.user import User


LEVEL_LABELS = {
    "beginner": "совсем новичок",
    "some": "знает базу, но путается",
    "pro": "уже писал небольшие программы",
}

GOAL_LABELS = {
    "course": "уверенно пройти основы Python",
    "exam": "подготовиться к экзамену или учёбе",
    "projects": "научиться писать свои проекты",
}

LEARNING_STYLE_LABELS = {
    "simple": "объяснять максимально простыми словами",
    "examples": "объяснять через примеры из жизни",
    "concise": "объяснять коротко и по делу",
}

PRACTICE_STYLE_LABELS = {
    "step_by_step": "давать пошаговые задачи",
    "challenge": "давать задачи с небольшим вызовом",
    "projects": "давать задачи ближе к мини-проектам",
}

AI_HELP_STYLE_LABELS = {
    "hints": "давать подсказки, но не готовое решение",
    "debug": "сначала разбирать ошибку и причину",
    "full_explain": "можно показывать решение и подробно разбирать его",
}

PACE_LABELS = {
    "calm": "спокойный темп, без спешки",
    "normal": "обычный темп",
    "fast": "быстрый темп, больше практики",
}


def build_user_profile_prompt(user: User | None) -> str:
    if not user:
        return (
            "Профиль ученика неизвестен.\n"
            "Объясняй нейтрально: просто, кратко, с небольшими примерами.\n"
        )

    return f"""
Профиль ученика:
- уровень: {LEVEL_LABELS.get(user.level, "не указан")}
- цель: {GOAL_LABELS.get(user.goal, "не указана")}
- стиль объяснений: {LEARNING_STYLE_LABELS.get(user.learning_style, "не указан")}
- стиль практики: {PRACTICE_STYLE_LABELS.get(user.practice_style, "не указан")}
- стиль помощи AI: {AI_HELP_STYLE_LABELS.get(user.ai_help_style, "не указан")}
- темп: {PACE_LABELS.get(user.pace, "не указан")}
- минут в день: {user.daily_minutes or "не указано"}

Как адаптироваться:
- учитывай уровень ученика;
- не перегружай терминами;
- если уровень beginner — объясняй маленькими шагами;
- если цель projects — чаще связывай тему с мини-проектами;
- если цель exam — добавляй проверочные вопросы и акцент на типичные ошибки;
- если стиль помощи hints — не давай готовый ответ сразу;
- если стиль помощи full_explain — можно дать решение, но обязательно объясни его;
- если темп calm — не ускоряйся и не давай много информации за раз;
- если темп fast — добавляй больше практических задач.
""".strip()